function getMatchedValueByKey(array, key, name_kr) {
  for (let index = 0; index < array.length; index++) {
    if (name_kr === array[index].name_kr) {
      return array[index][key];
    } else if (name_kr === array[index].search) {
      return array[index][key];
    }
  }

  return "내용이 없습니다.";
}

